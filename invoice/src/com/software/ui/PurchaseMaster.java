public void findSeller(){
    String sellerCode=jTextField13.getText().trim();
    String query="select seller_name from seller_master where trim(seller_code)='"+sellerCode+"'";
    String sellerName=null;
    try{
        Connection conn=new ConnDB().make_connection();
        Statement stmt=conn.createStatement();
       
        ResultSet rs=stmt.executeQuery(query);
        
        int counter=0;
        while(rs.next()){
            
            sellerName=rs.getString(1);
            counter++;
        }
        if(counter==0){
                    JOptionPane.showMessageDialog(this, "Seller Code does not exist! ");
                    jTextField13.requestFocus();
                    jTextField13.setText("");

        }
        else{
            
                        int selectedOption = JOptionPane.showConfirmDialog(null, 
                                  "Seller Name-"+sellerName+", Want to proceed?", 
                                  "Choose", 
                                  JOptionPane.YES_NO_OPTION); 
                    if (selectedOption == JOptionPane.YES_OPTION) {
                        jTextField12.requestFocus();
                    }
                    else{
                        jTextField13.setText("");
                        jTextField13.requestFocus();
                    }

            
            
        }
    
    
}catch(SQLException ex){
        
        JOptionPane.showMessageDialog(this, "Error Saving the data! ");
        ex.printStackTrace();
    }

}


public void fetchInvoice(){
    String fromDate=" "; 
     SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
      try{
            fromDate=sdf.format(jDateChooser1.getDate());
            
            
        }catch(NullPointerException ex)
        {
            ex.printStackTrace();
        }
     
    
    jComboBox1.removeAllItems();
    
    String query="select invoice_no from bill_master where invoice_date=str_to_date('"+fromDate+"','%d-%m-%Y')";
    try{
        Connection conn=new ConnDB().make_connection();
        Statement stmt=conn.createStatement();
        ResultSet rs=stmt.executeQuery(query);
        int counter=0;
        while(rs.next()){
            jComboBox1.addItem(rs.getString(1));
            counter++;
        }
        if(counter==0){
                    JOptionPane.showMessageDialog(this, "No Invoice found! ");
                    
        }
        else{
            jComboBox1.requestFocus();
        }
            
        
        }catch(SQLException ex){
        
        JOptionPane.showMessageDialog(this, "Error loading Company Code! ");
        ex.printStackTrace();
    }
}



